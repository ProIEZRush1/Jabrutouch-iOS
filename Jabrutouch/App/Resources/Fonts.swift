import UIKit

class Fonts {
    class func regularFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Regular", size: size)!
    }
    
    class func mediumDisplayFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Medium", size: size)!
    }
    
    class func mediumTextFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Medium", size: size)!
    }
    
    class func boldFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Bold", size: size)!
    }
    
    class func heavyFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Heavy", size: size)!
    }
    
    class func blackFont(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Black", size: size)!
    }
    
    class func semiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Semibold", size: size)!
    }
    
    
}
